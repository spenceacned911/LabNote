@dataclass(slots=True)
class I18n:
    language: str = "zh-CN"

    def set_language(self, language: str) -> None:
        self.language = language if language in TRANSLATIONS else "zh-CN"

    def tr(self, key: str, **kwargs: object) -> str:
        bundle = TRANSLATIONS.get(self.language, TRANSLATIONS["zh-CN"])
        fallback = TRANSLATIONS["en-US"]
        template = bundle.get(key, fallback.get(key, key))
        return template.format(**kwargs)

    def language_label(self, language: str) -> str:
        return self.tr(f"lang_label_{language}")

    def mode_label(self, mode: str) -> str:
        return self.tr(f"mode_label_{mode}")
